def run_git(root: Path, *args: str) -> bool:
    return subprocess.run(
        ["git", *args],
        cwd=root,
        stdout=subprocess.DEVNULL,
        stderr=subprocess.DEVNULL,
        check=False,
    ).returncode == 0


def md5(path: Path) -> str:
    digest = hashlib.md5()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def read_fasta(path: Path) -> dict[str, dict[str, str]]:
    records: dict[str, dict[str, str]] = {}
    if not path.exists():
        return records
    current_id = ""
    current_header = ""
    seq_parts: list[str] = []

    def flush() -> None:
        if current_id:
            records[current_id] = {
                "header": current_header,
                "sequence": "".join(seq_parts),
                "length": str(len("".join(seq_parts))),
            }

    with path.open() as handle:
        for line in handle:
            if line.startswith(">"):
                flush()
                current_header = line[1:].strip()
                current_id = current_header.split()[0]
                seq_parts = []
            else:
                seq_parts.append(line.strip())
    flush()
    return records


def header_annotation(header: str) -> str:
    if not header:
        return ""
    body = header.split(" ", 1)[1] if " " in header else ""
    return body.split(" n=", 1)[0]


def header_taxonomy(header: str) -> str:
    if "Tax=" not in header:
        return ""
    text = header.split("Tax=", 1)[1]
    return text.split(" TaxID=", 1)[0]


def qc_length(row: dict[str, str] | None) -> str:
    if not row:
        return ""
    return row.get("L_seq", "")


def qc_cov(row: dict[str, str] | None) -> str:
    if not row:
        return ""
    return row.get("cov_best", row.get("cov_hmm", ""))


def write_generated_manifest(root: Path) -> None:
    out_path = root / "results/02_qc_len255/generated_artifacts_manifest.tsv"
    columns = [
        "relative_path",
        "artifact_role",
        "file_size_bytes",
        "md5",
        "tracked_by_git",
        "ignored_by_git",
        "required_for_next_stage",
        "recreate_command_or_source",
        "notes",
    ]
    with out_path.open("w", newline="") as handle:
        writer = csv.DictWriter(handle, delimiter="\t", fieldnames=columns)
        writer.writeheader()
        for rel_path, role, required, recreate, notes in GENERATED_ARTIFACTS:
            path = root / rel_path
            tracked = run_git(root, "ls-files", "--error-unmatch", rel_path)
            ignored = run_git(root, "check-ignore", "-q", rel_path)
            writer.writerow(
                {
                    "relative_path": rel_path,
                    "artifact_role": role,
                    "file_size_bytes": str(path.stat().st_size) if path.exists() else "",
                    "md5": md5(path) if path.exists() else "",
                    "tracked_by_git": str(tracked).lower(),
                    "ignored_by_git": str(ignored).lower(),
                    "required_for_next_stage": str(required).lower(),
                    "recreate_command_or_source": recreate,
                    "notes": notes if path.exists() else f"MISSING: {notes}",
                }
            )


