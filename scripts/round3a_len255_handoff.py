def write_flagged_adjudication(root: Path) -> None:
    qc = read_qc(root / "results/02_qc_len255/qc_classification_Ib.tsv")
    input_records = read_fasta(root / "results/01_mining/hits_Ib_clean.fasta")
    nr80_hits = parse_cdhit_clusters(root / "results/02_qc_len255/nr80_Ib.fasta.clstr")
    columns = [
        "flag_class",
        "seq_id",
        "length",
        "cov_best",
        "annotation_or_header",
        "nr80_status",
        "nr80_representative",
        "identity_if_absorbed",
        "recommended_action",
        "notes",
    ]
    out_path = root / "results/02_qc_len255/len255_flagged_rescue_adjudication.tsv"
    with out_path.open("w", newline="") as handle:
        writer = csv.DictWriter(handle, delimiter="\t", fieldnames=columns)
        writer.writeheader()
        for flag_class, seq_id in FLAGGED_RESCUES:
            row = qc.get(seq_id, {})
            hit = nr80_hits.get(seq_id)
            if hit is None:
                nr80_status = "absent_after_nr80"
                rep = ""
                identity = ""
            elif hit.is_representative:
                nr80_status = "direct_nr80_representative"
                rep = hit.representative
                identity = ""
            else:
                nr80_status = "absorbed_by_nr80_surrogate"
                rep = hit.representative
                identity = hit.identity
            if flag_class == "KDOPS-like":
                action = "manual_review_before_final_tree"
            elif flag_class == "hypothetical/uncharacterized":
                action = "manual_review_before_final_tree"
            else:
                action = "needs_annotation_check"
            writer.writerow(
                {
                    "flag_class": flag_class,
                    "seq_id": seq_id,
                    "length": row.get("L_seq", ""),
                    "cov_best": qc_cov(row),
                    "annotation_or_header": input_records.get(seq_id, {}).get("header", ""),
                    "nr80_status": nr80_status,
                    "nr80_representative": rep,
                    "identity_if_absorbed": identity,
                    "recommended_action": action,
                    "notes": "Flag only; no sequence removed in Round 3A.",
                }
            )


